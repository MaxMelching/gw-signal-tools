                # return wf2

    def loss(args):
        return 1.-overlap(wf1, wf2_shifted(args), **inner_prod_kwargs)
    
    init_guess = np.zeros(len(opt_params))
    
    # init_guess = np.zeros(_opt_params.shape)
    # if 'psi' in _opt_params and 'phi_ref' in _opt_params:
